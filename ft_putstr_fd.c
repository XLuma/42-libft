#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	len;
	int	i;

	i = 0;
	len = ft_strlen(s);
	while(s[i] != '\0')
	{
		ft_putchar_fd(s[i], fd);
		i++;
	}
}
