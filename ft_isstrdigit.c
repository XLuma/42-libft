#include "libft.h"

int	ft_isstrdigit(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 48 && str[i] <= 57)
			i++;
		else
			return (1);
		i++;
	}
	return (0);
}
